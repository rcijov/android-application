package com.simona.halep.Database.Entities;

public class Stats {

	  private long id;
	  private String stat;
	  private String nrStat;

	  public long getId() {
	    return id;
	  }

	  public void setId(long id) {
	    this.id = id;
	  }
	  
	  public String getStat() {
	    return stat;
	  }

	  public void setStat(String stat) {
	    this.stat = stat;
	  }
	  
	  public String getNrStat() {
	    return nrStat;
	  }

	  public void setNrStat(String nrStat) {
	    this.nrStat = nrStat;
	  }
	  
}
